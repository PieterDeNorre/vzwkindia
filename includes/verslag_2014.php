	<p>Bij het binnenrijden van de stad Ranchi, hoofdstad van de nieuwe staat Jharkhand, hebben we het geluk een stoet van de &ldquo;Chhath Puja&rdquo; te zien. Dit is een rituele aanbidding door en voor vrouwen.</p>
	<p>Terwijl ze bij zonsopgang in het water staan met een brandende lamp, gedurende twee uren, moeten ze bidden tot de godin. De mannen dragen verschillende soorten fruit in manden op hun hoofd en brengen die naar de Puja plaats. Op zo een dag kost het fruit 100x meer dan op een normale dag. Een vb. 1 doz bananen kost normaal 10 tot 15 Rs. en vandaag 100 Rs.</p>
	<p>De banaan is het belangrijkste fruit voor deze Puja.</p>
	<p>Bij Don Bosco Kokar, Ranchi worden we verwelkomd door F. VP Jose en de studenten van het internaat. Er is een technische school met 2502 leerlingen, een Engelse Middelbare school van KG tot klas X, en nacht coaching klassen voor de arme kinderen in de buurt en in Garigaan op 5 km afstand een kleine school met 600 arme kinderen. F. Jyotisch is het hoofd van de Engelse Middelbare school.</p>
